function clickOnce(elem){
  elem.disabled = 'disabled';
  elem.value = elem.getAttribute('click-once-text');
  var form = getClosest(elem, 'form');
  form.submit();
  return false;
}

function getClosest(el, tag) {
  // this is necessary since nodeName is always in upper case
  tag = tag.toUpperCase();
  do {
    if (el.nodeName === tag) {
      // tag name is found! let's return it. :)
      return el;
    }
  } while (el = el.parentNode);

  // not found :(
  return null;
};

